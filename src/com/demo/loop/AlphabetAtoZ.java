package com.demo.loop;

public class AlphabetAtoZ {

	public static void main(String[] args) {
		char ch;
		for (ch = 'a'; ch <= 'z'; ch++) {
			System.out.println("Smalll alphabet: " + ch);
		}
		for (ch = 'A'; ch <= 'Z'; ch++) {
			System.out.println("Capital alphabet: " + ch);
		}

	}

}
