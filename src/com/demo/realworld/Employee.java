package com.demo.realworld;

public class Employee {
	private int salary, id;
	private String name;

	public void setId(int x) {
		id = x;
	}

	public int getId() {
		return id;
	}

	public void setSalary(int x) {
		salary = x;
	}

	public int getSalary() {
		return salary;
	}

	public void setName(String x) {
		name = x;
	}

	public String getName() {
		return name;
	}

}
